package com.java.ejercicios.Sesion3;

public class Gato {

	public static boolean isCatPlaying(boolean verano, int temperatura) {
		if(verano = false && temperatura > 24 && temperatura < 36) {
			return true;
		}else if(verano = true && temperatura > 24 && temperatura < 46) {
			return true;
		}else { 
			return false;
		}
		
	}

}
